import streamlit as st
import pickle
import pandas as pd

# Load model and data
model = pickle.load(open('model.pkl', 'rb'))
df = pd.read_csv("/content/Bengaluru_House_Data.csv")

# Clean for location dropdown
df = df.dropna()
locations = sorted(df['location'].unique())

# UI
st.title("üè° Bengaluru House Price Prediction")
st.write("Enter the details below:")

location = st.selectbox("Location", locations)
sqft = st.number_input("Total Square Feet", min_value=300)
bhk = st.number_input("Number of Bedrooms (BHK)", min_value=1, step=1)
bath = st.number_input("Number of Bathrooms", min_value=1, step=1)

if st.button("Predict Price"):
    # Basic feature encoding
    x = pd.DataFrame([[sqft, bath, bhk] + [1 if loc == location else 0 for loc in locations if loc != locations[0]]])
    prediction = model.predict(x)[0]
    st.success(f"Estimated Price: ‚Çπ {round(prediction, 2)} Lakhs")